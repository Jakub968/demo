let photo1 = document.getElementsByClassName('photo1')
let text1 = document.getElementsByClassName('text1')

photo1[0].addEventListener("mouseover", () => {
    text1[0].style.opacity = '1'
    text1[0].style.top = '45%'
} )

photo1[0].addEventListener("mouseout", () => {
    text1[0].style.opacity = '0'
    text1[0].style.top = '20%'
} )


